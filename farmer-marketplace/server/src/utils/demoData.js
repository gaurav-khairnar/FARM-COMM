// Helper function to generate listing data
function generateListingData(product, daysUntilExpiry = 5) {
  const expiryDate = new Date();
  expiryDate.setDate(expiryDate.getDate() + daysUntilExpiry);
  
  const priceRanges = {
    'Vegetables': { min: 30, max: 80 },
    'Fruits': { min: 50, max: 150 },
    'Grains': { min: 40, max: 120 },
    'Dairy': { min: 40, max: 90 },
    'Others': { min: 30, max: 100 }
  };
  
  const range = priceRanges[product.category] || priceRanges['Others'];
  const price = Math.floor(Math.random() * (range.max - range.min + 1)) + range.min;
  const quantity = Math.floor(Math.random() * 50) + 10;
  
  return {
    price,
    quantity,
    unit: 'kg',
    expiryDate: expiryDate.toISOString()
  };
}

